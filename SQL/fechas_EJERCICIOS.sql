        WHEN  EDAD >= DATE_ADD(CLI.FECHA_NACIMIENTO, INTERVAL 60 YEAR) AND EDAD <= DATE_ADD(CLI.FECHA_NACIMIENTO, INTERVAL 74 YEAR) THEN "adulto mayor"
        WHEN  EDAD >= DATE_ADD(CLI.FECHA_NACIMIENTO, INTERVAL 75 YEAR) AND EDAD <= DATE_ADD(CLI.FECHA_NACIMIENTO, INTERVAL 90 YEAR) THEN "anciano"
        WHEN  EDAD > DATE_ADD(CLI.FECHA_NACIMIENTO, INTERVAL 90 YEAR) THEN "anciano longevo"
	END AS "La Organización Mundial de la Salud (OMS), por su parte, clasifica la edad adulta de la siguiente manera:"
FROM CLIENTE CLI;

