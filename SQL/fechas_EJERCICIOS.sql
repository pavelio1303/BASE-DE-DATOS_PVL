# EJ13 - MUESTRA LA CANTIDAD DE SINIESTROS QUE HAN OCURRIDO PARA CADA DÍA DE LA SEMANA. ORDENA DE MAYOR A MENOR.
USE SEGUROS;

SELECT
    DATE_FORMAT(FECHA, "%W") AS DIA_SEMANA,
    COUNT(*) AS SINIESTROS
FROM SINIESTRO
GROUP BY DIA_SEMANA
ORDER BY SINIESTROS;

