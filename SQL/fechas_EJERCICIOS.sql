# EJ16 - ¿HAY ALGÚN CLIENTE CUYA FECHA DE NACIMIENTO SEA POSTERIOR A LA FECHA DE MATRICULACIÓN DE SU VEHÍCULO?
SELECT 
	CLI.DNI,
    CLI.NOMBRE
FROM CLIENTE CLI
JOIN CLIENTE_ASEGURA_VEHICULO CAV ON CLI.DNI = CAV.DNI_CLIENTE
JOIN VEHICULO VEH ON CAV.NUMERO_BASTIDOR_VEHICULO = VEH.NUMERO_BASTIDOR
WHERE CLI.FECHA_NACIMIENTO > VEH.FECHA_1_MATRICULACION
GROUP BY CLI.DNI
ORDER BY CLI.NOMBRE;

